const mongoose = require("mongoose");

const contractorSchema = new mongoose.Schema({
  contractorID: {
    type: String,
    required: true,
    unique: true,
  },
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
  address: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    required: true,
  },
  established: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model("Contractor", contractorSchema);
